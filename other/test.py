# 用来临时检查一些程序

import numpy as np

data = [27.75, 39.62, 26.88, 24.50, 23.88, 32.00, 25.00, 40.62, 36.12, 28.62]
data = [77.99, 84.59, 85.07, 86.39, 86.74, 82.22, 86.11, 77.43, 79.93, 84.10]
data = [ 9.08,  4.36,  5.13,  5.94,  5.94,  7.67,  5.44,  8.79,  8.20,  4.87]
avg = np.mean(data)        # 平均值
std = np.std(data, ddof=1) # 标准差（样本标准差，ddof=1）
print(f'平均值: {avg:.2f}, 标准差: {std:.2f}')